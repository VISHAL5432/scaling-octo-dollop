package HashSet_;

import java.util.HashSet;

public class HashSet_01 {

	public static void main(String[] args) {
		 HashSet ht = new HashSet();
		 ht.add("Mohan");
		 ht.add("mohan");
		 ht.add("Sohan");
		 ht.add("Solu");
		 ht.add(" ");
		 ht.add("monu");
		 System.out.println(ht);
		 HashSet hs = new HashSet();
		 hs.add("Solu");
		 hs.add(" ");
		 hs.add("Monu");
		 hs.add("Mohan");
		 hs.add("mohan");
		 hs.add("Sohan");
		
		 System.out.println(hs);
		 

	}

}
